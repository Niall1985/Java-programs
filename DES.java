    static String encrypt(String plaintext, String key) {
        byte[] padded = pad(plaintext.getBytes());
        List<long[]> subKeys = generateSubKeys(key);

        StringBuilder cipher = new StringBuilder();
        for (int i = 0; i < padded.length; i += 8) {
            long block = bytesToLong(Arrays.copyOfRange(padded, i, i + 8));
            cipher.append(Long.toHexString(desBlock(block, subKeys, true)));
        }
        return cipher.toString();
    }

    static String decrypt(String ciphertext, String key) {
        List<long[]> subKeys = generateSubKeys(key);
        Collections.reverse(subKeys);

        StringBuilder plain = new StringBuilder();
        for (int i = 0; i < ciphertext.length(); i += 16) {
            long block = Long.parseUnsignedLong(ciphertext.substring(i, i + 16), 16);
            plain.append(new String(longToBytes(desBlock(block, subKeys, false))));
        }
        return plain.toString().trim();
    }

    static long desBlock(long block, List<long[]> keys, boolean encrypt) {
        block = permute(block, IP, 64);
        int L = (int)(block >>> 32);
        int R = (int)block;

        for (long[] k : keys) {
            int temp = R;
            R = L ^ feistel(R, k[0]);
            L = temp;
        }

        long preOutput = (((long)R) << 32) | (L & 0xffffffffL);
        return permute(preOutput, FP, 64);
    }
    


    static int feistel(int R, long K) {
        long expanded = permute(R & 0xffffffffL, E, 32);
        expanded ^= K;

        int out = 0;
        for (int i = 0; i < 8; i++) {
            int sixBits = (int)((expanded >>> (42 - i * 6)) & 0x3F);
            int row = ((sixBits & 0x20) >> 4) | (sixBits & 1);
            int col = (sixBits >> 1) & 0xF;
            out = (out << 4) | SBOX[i][row][col];
        }
        return (int)permute(out & 0xffffffffL, P, 32);
    }


